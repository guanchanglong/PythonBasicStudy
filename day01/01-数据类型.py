# 变量的数据类型，由变量中存储的结构决定的
# 可以使用 type() 函数得到变量的数据类型，想要进行输出需要使用 print() 函数

# int 整数
age = 18
print(type(age))  # <class 'int'>类型

# float 小数
result = 3.14
print(type(result))  # <class 'float'>

# str，字符串，单双引号都可
name = '小明'
print(type(name))  # <class 'str'>

# bool 布尔类型，只有两个值 True、False
result = True  # 修改变量中存储的数据
print(type(result))  # <class 'bool'>

